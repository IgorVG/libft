#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int l;

	l = n;
	if (n < 0)
	{
		l *= -1;
		ft_putchar_fd('-', fd);
	}
	if (l / 10)
		ft_putnbr_fd(l / 10, fd);
	ft_putchar_fd((l % 10) + 48, fd);
}
