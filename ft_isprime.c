int		ft_isprime(long int i)
{
	long int d;

	if (i < 2)
		return (0);
	if (i == 2 || i == 3)
		return (1);
	d = 2;
	while (d * d <= i)
	{
		if (i % d == 0)
			return (0);
		d++;
	}
	return (1);
}
